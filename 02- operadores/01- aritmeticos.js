let a = 5;
let b = 7;
console.log(a + b, 'suma') //suma;
console.log(a - b, 'resta') //resta;
console.log(a * b, 'multiplicacion') //multiplicacion;
console.log(a / b, 'division') //division;
console.log(a % b, 'modulo') //modulo;
console.log(a ** b, 'potencia o exponenciacion') //potencia o exponenciacion;

//?incremento
console.log('==========incremento==========')
console.log(a, 'numero normal')
console.log(++a, 'aumentando y mostrando');
console.log(a++, 'mostrando y aumentando abajo');
console.log(a, 'resultado final')
//?decremento
console.log('==========decremento==========')
console.log(b, 'numero normal')
console.log(--b, 'reduciendo y mostrando');
console.log(b--, 'mostrando y reduciendo abajo');
console.log(b, 'resultado final')